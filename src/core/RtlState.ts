/**
 * تجريد لتخزين حالة الإضافة الدائمة (هل RTL مفعَّل؟).
 * يفصل المنطق عن `vscode.Memento` ليبقى قابلًا للاختبار.
 */
export interface RtlState {
  /** هل طلب المستخدم تفعيل RTL (آخر نيّة مسجّلة)؟ */
  isEnabled(): boolean;
  /** تسجيل نيّة التفعيل/التعطيل. */
  setEnabled(value: boolean): Promise<void>;
}

/** نتيجة فحص الانحراف بين النيّة المسجّلة والحالة الفعليّة على القرص. */
export type DriftStatus =
  | 'ok' // الحالة الفعليّة تطابق النيّة
  | 'drift' // المستخدم فعّل RTL لكنّه غير مطبَّق فعليًّا (تحديث VS Code محا الحقن)
  | 'stale'; // RTL مطبَّق فعليًّا لكنّ النيّة المسجّلة هي التعطيل
